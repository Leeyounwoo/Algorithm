'''
입력)
0000000111100000011000000111100110000110000111100111100111111001100111

출력)
0,120,12,7,76,24,60,121,124,103
'''

num = '0000000111100000011000000111100110000110000111100111100111111001100111'

for i in range(0, len(num) - 7 + 1, 7):
    bin = num[i:i+7]  # ex) 0000000, 1111000 ...

    total = 0
    for j in range(len(bin)-1, -1, -1):  # ex) 6, 5, 4, 3, 2, 1, 0
        if bin[j] == '1':
            total += 2 ** (len(bin) - 1 - j)  # ex) 2**0, 2**1, 2**2 ...

    print(total, end=" ")

